                <dt class="heading-6">ARTICULO 1. – INSCRIPCIONES:</dt>
                <dd>
                    El aspirante a pertenecer a la ̈"ACADEMIA DE FORMACION DE FUTBOL" deberá entrevistarse con un
                    representaste directivo de la institución, para conocer inquietudes reciprocas y aspectos importantes
                    del jugador y su familia. Los
                    requisitos para ingresar son los siguientes:
                    <ol>
                        <li> 1. Llenar la solicitud de admisión o registro del alumno con datos fidedignos, cualquier
                            falsedad
                            en la información será causa
                            de baja definitiva.</li>
                        <li>
                            2. Llenar y firmar el expediente médico.
                        </li>
                        <li>
                            3. Leer y firmar de conformidad del reglamento.
                        </li>
                        <li>
                            4. Copia del certificado de nacimiento y copia de C.I.
                        </li>
                        <li>
                            5. Foto tamaño carnet fondo rojo con uniforme de la ̈ESCUELA DE FUTBOL ̈ MEDIO MAGNETICO.
                        </li>
                    </ol>
                </dd>
                <dt class="heading-6">
                    ARTICULO 2. – REPRESENTACION:
                </dt>
                <dd>
                    En todos los eventos deportivos en que participen los equipos de la ACADEMIA DE FUTBOL,
                    deberán presentarse indistintamente con el nombre de la: ̈ACADEMIA DE FUTBOL R.T. NUEVAS ESTRELLAS ̈.
                </dd>
                <dt class="heading-6">ARTICULO 3.- EQUIPOS:</dt>
                <dd>Todas las categorías, tendrán registrados, un número máximo de 20 veinte jugadores.
                    Todos los equipos deberán dar preferencia en su participación a la liga oficial de futbol local; torneos
                    de invitación, además de
                    entrega de reconocimientos u otros acontecimientos que la ACADEMIA DE FUTBOL comunique.
                    Los equipos se sujetarán al horario de entrenamiento que les asigne la academia.
                </dd>
                <dt class="heading-6">
                    ARTICULO 4.- ASISTENCIA Y PUNTUALIDAD:
                </dt>
                <dd>
                    <ul>
                        <li>•
                            Los jugadores están obligados a ser puntuales en los horarios de entrenamiento o citas
                            especiales del entrenador o
                            coordinador de la ACADEMIA.
                        </li>
                        <li>•
                            En cada entrenamiento se tomará lista de asistencia.
                        </li>
                        <li>•
                            La cita para los entrenamientos será de 5 minutos antes de la hora oficial del mismo y se
                            otorgará una tolerancia de 10
                            minutos como máximo.
                        </li>
                        <li>•
                            Como situación especial, no como costumbre, al cumplir 3 tardanzas en un mes se les enviara un
                            aviso de suspensión,
                            exceptuando causas justificadas.
                        </li>
                        <li>•
                            Mas de 5 faltas al mes es causa de baja definitiva.
                        </li>
                        <li>•
                            Es muy importante tomar en cuenta la hora de entrada y salida de los niños, ya que los
                            entrenadores serán responsables
                            dentro del horario oficial de entrenamiento, con una tolerancia de 10 minutos máximo después del
                            término del mismo.
                        </li>
                        <li>•
                            Se suspenderá el entrenamiento o el juego, en caso de lluvia muy fuerte, tormentas eléctricas,
                            motivos de fuerza mayor.
                        </li>
                        <li>•
                            Los juegos de competencia se efectuarán en el campo y en el horario que determine la liga o
                            competencia donde se
                            encuentren afiliados los equipos de la ACADEMIA.
                        </li>
                        <li>•
                            Los jugadores o los padres de familia, tendrán la obligación de reportarse un día antes del
                            juego, para enterarse de la hora
                            y lugar del mismo, con el representante de su equipo. Niño que no se reporte sin causa
                            justificada y no asista al partido, se
                            hará acreedor de una amonestación.
                        </li>
                        <li>•
                            Los jugadores deberán llegar 40 minutos antes de empezar un partido de competencia; los niños
                            que lleguen tarde y estén
                            dentro de la planilla titular, se quedarán en la banca el primer tiempo del partido y aquellos
                            que estén en la planilla de
                            suplentes no podrán jugar.
                        </li>
                        <li>•
                            Jugador que acumule tres amonestaciones, será suspendido un juego; no obstante, tendrá la
                            obligación de presentarse al
                            partido de competencia, o en su defecto, se hará acreedor a otra suspensión.
                        </li>

                    </ul>
                </dd>
                <dt class="heading-6">ARTICULO 5.- DISCIPLINA:</dt>
                <dd>
                    <ul>
                        <li>•
                            Todo jugador, así como el padre de familia están obligados a guardar el debido respeto hacia las
                            personas que estén en las
                            instalaciones y acatar las instrucciones de los directivos y entrenadores.
                        </li>
                        <li>•
                            Cualquier situación fuera de lo normal será analizada por medio del directivo.
                        </li>
                        <li>•
                            Los jugadores y padres evitaran que otras ocupaciones o personas interferirán o interrumpan el
                            cumplimiento del
                            entrenamiento, así como la labor formativa de los entrenadores.
                        </li>
                        <li>•
                            Los jugadores en su trato cotidiano lo harán en forma cordial y compañerismo, evitando palabras
                            obscenas y ofensivas, así
                            como malas bromas y el juego sucio y peligroso, en caso de incurrir en este punto se emitirá un
                            informe de la situación para
                            tomar las acciones pertinentes.
                        </li>
                        <li>•
                            Los jugadores deben hacer uso del campo y de las instalaciones que indique el entrenador o
                            coordinador.
                        </li>
                        <li>•
                            Cualquier acto de indisciplina será sancionado por la directiva según la gravedad, el alumno
                            podrá causar baja definitiva.
                        </li>
                        <li>•
                            Los papas deben respetar la charla previa y de medio tiempo con los niños en cada juego sin su
                            presencia para evitar que
                            se cohíban (no intentar ser un segundo entrenador)
                        </li>
                        <li>•
                            Queda estrictamente prohibido dirigirse al árbitro para reclamarles decisiones arbitrales. En
                            todo caso dirigirse con el
                            entrenador o coordinador.
                        </li>
                    </ul>
                </dd>
                <dt class="heading-6">ARTICULO 6.- UNIFORMES:</dt>
                <dd>
                    <ul>
                        <li>•
                            Los jugadores están obligados a aportar siempre con pulcritud el uniforme oficial de la
                            Academia, debiendo presentarse a
                            la hora y fecha del entrenamiento y partido.
                        </li>
                        <li>•
                            Los jugadores deberán presentarse con tenis saloneros y/o chuteras para el futbol.
                        </li>
                    </ul>
                </dd>

                <dt class="heading-6">ARTICULO 7.- ACCESO A LAS INSTALACIONES:</dt>
                <dd>
                    <ul>
                        <li>•
                            Se les entregara una credencial para el acceso a las instalaciones.
                        </li>
                        <li>•
                            Cada entrenador es responsable del grupo asignado dentro del horario del mismo.
                        </li>
                        <li>•
                            Procurar que el jugador no consuma alimentos pesados ni bebidas por lo menos dos horas antes de
                            cada entrenamiento,
                            ya que puede producir malestar y dolores abdominales. Así mismo se debe procurar enseñarles a ir
                            al baño antes de llegar
                            al entrenamiento, ambas acciones evitarán riesgos para el jugador, ya que permanecerán dentro
                            del campo todo el tiempo.
                        </li>
                        <li>•
                            En caso de malestar, dolor o lesión se dará aviso a los padres o familiares inmediatamente.
                        </li>
                        <li>•
                            Es de vital importancia que en caso de cualquier enfermedad que ponga en riesgo la salud del
                            niño o de sus compañeros
                            sea notificada al coordinador y/o entrenador.
                        </li>
                        <li>•
                            Queda estrictamente prohibido el acceso de mascotas o juguetes dentro de las instalaciones.
                        </li>
                    </ul>
                </dd>

                <dt class="heading-6">ARTICULO 8.- PAGOS:</dt>
                <dd>
                    <ul>
                        <li>•
                            En caso de adeudo, el alumno podrá realizar su entrenamiento en forma Los pagos mensuales se
                            deberán hacer
                            dentro de los primeros 5 cinco días de cada mes; si se paga la mensualidad después de ese día
                            causara un recargo
                            que la directiva de la ACADEMIA dispondrá cada mes que se retrase en el pago, esto con el
                            propósito de evitar
                            morosidad.
                        </li>

                        <li>•
                            normal y se le enviara un aviso en el siguiente entrenamiento, deberá haber sido cubierto su
                            pago ya sea de
                            mensualidad, uniforme deportivo, etc. Para poder participar con normalidad.
                        </li>

                        <li>•
                            Es motivo de suspensión el adeudo de un mes de mensualidad.
                        </li>

                        <li>•
                            Los comprobantes de pago son importantes para cualquier aclaración hacia el coordinador o
                            directivo.
                        </li>
                        <li>•
                            Todo el personal involucrado con los alumnos tendrá la obligación de mantener y seguir los
                            planes que la
                            coordinación le proporcione para facilitar la práctica.
                        </li>
                        <li>•
                            El personal de la ACADEMIA tendrá la obligación de mantener el respeto y la educación hacia los
                            alumnos y padres
                            de familia.
                        </li>

                        <li>•
                            El entrenador tendrá que estar pendiente del grupo asignado por lo cual deberá estar presente
                            por lo menos 10
                            minutos antes de la hora del entrenamiento y hasta que salga el ultimo jugador, en el entendido
                            de que es obligación
                            de los padres llegar puntual a la cita de entrada y salida.
                        </li>



                    </ul>
                </dd>

                <dt class="heading-6">ARTICULO 9.- LA INSTITUCION:</dt>
                <dd>
                    <ul>
                        <li>•
                            Proporcionará el material deportivo para el desarrollo del entrenamiento según las necesidades.
                        </li>
                        <li>•
                            La ACADEMIA proporcionará el lugar donde se efectuará el entrenamiento.
                        </li>
                        <li>•
                            En caso de participaciones especiales dentro o fuera de la ciudad se les enviara un aviso para
                            solicitar su autorización y
                            permiso para que el alumno pueda jugar durante el torneo o competencia.
                        </li>
                    </ul>
                </dd>


                <dt class="heading-6">ARTICULO 10.- FALTAS GRAVES: </dt>
                <dd>
                    <ul>
                        <li>•
                            Las siguientes faltas se consideran graves y ameritan la separación parcial o definitiva del
                            alumno.
                        </li>
                        <li>•
                            Lesiones al honor y buen nombre de la institución por parte del alumno.
                        </li>
                        <li>•
                            Inmoralidad, mal comportamiento y malas costumbres dentro y fuera de las instalaciones.
                        </li>
                        <li>•
                            Hurto comprobado.
                        </li>
                        <li>•
                            Incumplimiento de las sanciones impuestas.
                        </li>
                        <li>•
                            Deterioro o destrucción del material y equipo de trabajo, así como de las instalaciones.
                        </li>
                    </ul>
                </dd>


                <dt class="heading-6">ARTICULO 11.- BECAS:</dt>
                <dd>
                    La ACADEMIA podrá otorgar becas a niños de escasos recursos, que demuestren capacidad, interés y
                    disciplina
                    en el Club; para apoyarles y exentarlos en todo o en parte, respecto de la obligación de la aportación
                    mensual. La beca se les eliminara
                    si dejan de tener interés, bajo rendimiento y no son cumplidos a los entrenamientos o los partidos.
                </dd>

                <dt class="heading-6">ARTICULO 12.- DERECHO DE ADMISION: </dt>
                <dd>
                    La ACADEMÍA se reserva en todo momento el derecho de admisión.
                </dd>

                <dt class="heading-6">ARTICULO 13.- OTROS: </dt>
                <dd>
                    Cualquier situación no prevista en este reglamento será resuelto por la directiva. El suscrito, padre
                    (s) de
                    familia del jugador que se menciona más adelante, después de leer y comprender el contenido del presente
                    reglamento, manifiesto
                    mi conformidad en cumplir el mismo; así mismo otorgo mi consentimiento para que mi menor hijo participe
                    en las diversas
                    competencias en que participe la ACADEMIA, haciéndome responsable del riesgo deportivo y de la atención
                    medica que requiera en
                    casos de lesiones, eximiendo a los directivos, coordinadores y entrenadores del club de responsabilidad
                    alguna por dicho concepto.
                </dd>


